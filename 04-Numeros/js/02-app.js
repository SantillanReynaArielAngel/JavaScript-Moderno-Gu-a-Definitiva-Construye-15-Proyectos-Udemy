
//Operaciones basicas con mumeros

const num1=10;
const num2=6;

let resultado;
//suma
resultado=num1+num2;
console.log(resultado);

//resta
resultado=num1-num2;
console.log(resultado);

//Multiplicacion
resultado=num1*num2;
console.log(resultado);

//Division
resultado=num1/num2;
console.log(resultado);

//Residuo o mudulo
resultado=num1%num2;
console.log(resultado);